package com.postgrid.magicspell;

/**
 * <h1>MSButton</h1>
 * MSButton enum class represents all the keys on the keyboard.
 * This includes the first ten letters (A-J), numbers (0-9) and
 * modifier keys.
 * <p>
 * The enum class is used to index the buttons appropriately
 * when used or pressed on.
 *
 * @author  connor
 * @version 0.1
 * @since   9/2/2016
 */

enum MSButton {
    BUTTON_1,
    BUTTON_2,
    BUTTON_3,
    BUTTON_4,
    BUTTON_5,
    BUTTON_6,
    BUTTON_7,
    BUTTON_8,
    BUTTON_9,
    BUTTON_10,
    BUTTON_SPACE,
    BUTTON_SHIFT,
    BUTTON_PERIOD,
    BUTTON_COMMA,
    BUTTON_BACKSPACE,
    BUTTON_NUMBERS,
    BUTTON_RESIZE,
    BUTTON_DONE
}
